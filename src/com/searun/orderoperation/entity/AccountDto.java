public class AccountDto implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2942332189453850029L;
	
	/**
	 * 唯一标示符
	 */
	private String accountID;

	/**
	 * 创建时间
	 */
	private String createTime;

	private String id;// ID

	private String showName;// 账户显示名称

	private String name;// 账户姓名

	private String accType;// 账户类型（关联数据字典表TYPE_CODE='ACC_TYPE'）

	private String accountNum;// 账户

	private String isDefault;// 是否默认 Y 默认 N 非默认

	private String remark;// 备注

	// 会员ID
	private String memberId;

	public AccountDto() {

	}
	
	

	public String getAccountID() {
		return accountID;
	}



	public void setAccountID(String accountID) {
		this.accountID = accountID;
	}



	public String getCreateTime() {
		return createTime;
	}



	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}



	public String getShowName() {
		return showName;
	}

	public void setShowName(String showName) {
		this.showName = showName;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAccType() {
		return accType;
	}

	public void setAccType(String accType) {
		this.accType = accType;
	}

	public String getAccountNum() {
		return accountNum;
	}

	public void setAccountNum(String accountNum) {
		this.accountNum = accountNum;
	}

	public String getIsDefault() {
		return isDefault;
	}

	public void setIsDefault(String isDefault) {
		this.isDefault = isDefault;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getMemberId() {
		return memberId;
	}

	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

}
