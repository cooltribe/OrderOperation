package com.searun.orderoperation.entity;

import java.io.Serializable;

/**
 * 车辆管理-线路
 * 
 * @author zhazhaobao
 * 
 */
public class CarManagerLineInfo implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * 起点
	 */
	private String from;

	/**
	 * 终点
	 */
	private String to;

	/**
	 * 路线状态
	 */
	private String lineType;

	/**
	 * 司机
	 */
	private DriverManagerInfo driver;

	/**
	 * 有效期限
	 */
	private String validTime;

	public String getFrom() {
		return from;
	}

	public void setFrom(String from) {
		this.from = from;
	}

	public String getTo() {
		return to;
	}

	public void setTo(String to) {
		this.to = to;
	}

	public String getLineType() {
		return lineType;
	}

	public void setLineType(String lineType) {
		this.lineType = lineType;
	}

	public DriverManagerInfo getDriver() {
		return driver;
	}

	public void setDriver(DriverManagerInfo driver) {
		this.driver = driver;
	}

	public String getValidTime() {
		return validTime;
	}

	public void setValidTime(String validTime) {
		this.validTime = validTime;
	}

}
